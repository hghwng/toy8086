org 0x100

; #0x21-#0x01 interrupt input, return al-code
mov ah, #0x01
int #0x21
sub al, #0x30 ; '0'
mov bl, al

mov ah, #0x02
mov dl, #0x20 ; ' '
int #0x21
mov dl, #0x2b ; '+'
int #0x21
mov dl, #0x20 ; ' '
int #0x21

mov ah, #0x01
int #0x21
sub al, #0x30

add bl, al

cmp bl, #9
jle le_nine
add bl, #0x7

le_nine:
add bl, #0x30
mov ah, #0x02
mov dl, #0x20 ; ' '
int #0x21
mov dl, #0x3d ; '='
int #0x21
mov dl, #0x20 ; ' '
int #0x21

; #0x21-#0x02 interrupt controls output, changes al=dl
mov ah, #0x02
mov dl, bl
int #0x21

mov dl, #0x0a
int #0x21

hlt

